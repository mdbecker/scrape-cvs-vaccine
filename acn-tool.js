if (window.DOMParser) {
    parser = new DOMParser();
    xmlDoc = parser.parseFromString(xml, "text/xml");
}
else //For IE
{
    xmlDoc = new ActiveXObject("Microsoft.XMLDOM");
    xmlDoc.async = false;
    xmlDoc.loadXML(xml);
}

var states = xmlDoc.getElementsByTagName("state");
var ddl = document.getElementById("ddlStates");

for (var i = 0; i < states.length; i++) {
    var option = document.createElement("option");

    //Set Customer Name in Text part.
    option.text = states[i].getElementsByTagName("name")[0].textContent;

    //Set Customer CustomerId in Value part.
    option.value = states[i].getElementsByTagName("id")[0].textContent;

    //Add the Option element to DropDownList.
    ddl.options.add(option);
}

function GetStateNumber(){
    var e = document.getElementById("ddlStates");
    var result = e.options[e.selectedIndex].text;
    var val = e.options[e.selectedIndex].value;
    if(val == ""){
        document.getElementById("resultBlock").style.display = "none";
    }
    else{
        for (var s = 0; s < states.length; s++){
            if(states[s].getElementsByTagName("id")[0].textContent == val){
                var hdatau = states[s].getElementsByTagName("hdatau")[0].textContent;
				var hinfo = states[s].getElementsByTagName("hinfo")[0].textContent;
                var hdatau1 = states[s].getElementsByTagName("hdatau1")[0].textContent;
				var hinfo1 = states[s].getElementsByTagName("hinfo1")[0].textContent;
				var cta = states[s].getElementsByTagName("cta")[0].textContent;
				var fhdatau1 = "<span class='no-link-state acn-result-text1' href='"+ hinfo1 +"'>" + hdatau1 + "</span>";
				if(cta == ""){
					var fcta = "";
				 }
				else{
					var fcta = "<a class='caret-red-black-text-16 cta-styling acn-result-text1' href='"+ hinfo1 +"'>" + cta + "</a>"
				}
				var fhdatau = "<a class='caret-red-black-text-16 acn-result-text1' href='"+ hinfo +"' target='_blank'>" + hdatau + "</a>";
            }
        }
        document.getElementById("resultBlock").style.display = "block";
		document.getElementById("scheduleData").style.display = "block";
		if(fcta == ""){
			document.getElementById("ctaData").style.display = "none";
		}
		else{
			document.getElementById("ctaData").style.display = "block";
		}
		document.getElementById("stateData").style.display = "block";
        document.getElementById("scheduleData").innerHTML = fhdatau1;
        document.getElementById("ctaData").innerHTML = fcta;
        document.getElementById("stateData").innerHTML = fhdatau;
    }
}